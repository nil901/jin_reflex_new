class Product {
  final String id;
  final String title;
  final String image;
  final double price;
  final String details;
  final String description;
  final String additionalInfo;

  Product({
    required this.id,
    required this.title,
    required this.image,
    required this.price,
    required this.details,
    required this.description,
    required this.additionalInfo,
  });
}
